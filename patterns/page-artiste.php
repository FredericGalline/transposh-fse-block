            <p>Hans Silvester, ancien grand reporter de l’agence Rapho, est aujourd’hui l’un des derniers grands photographes indépendants.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph -->
            <p>Hans Silvester est apprécié pour son aptitude à se fondre parmi ses sujets, à obtenir leur confiance et à sa capacité à investir le temps qu'il convient pour obtenir l'image qui s'inscrit dans l'histoire. Son œuvre incarne l'essence même du photographe, à savoir son rôle dans la transmission d'un patrimoine voué à la disparition, il traverse les 60 dernières années en préservant, par son travail un patrimoine menacé ou déjà disparu.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph -->
            <p>Parmi ses thèmes de prédilection figurent l’écologie, la défense de la planète, les photographies des animaux et des objets simples du quotidien. Il est mondialement reconnu pour son travail de longue haleine auprès des tribus du sud de l’Ethiopie - les peuples de l’Omo - témoignant de leurs coutumes ancestrales juste avant que la civilisation ne les rattrape.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph -->
            <p>Depuis son premier voyage en Provence et en Camargue en 1958, Hans Silvester ne cesse de parcourir le monde et de rapporter des photos magnifiques. Il est sollicité par les plus grands musées du monde, ou par les plus grands festival de photos comme visas pour l'image à Perpignan ou encore les rencontres d'Arles et nous sommes heureux et fiers de présenter régulièrement ses photographies.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph -->
            <p>Que ce soit au travers d'expositions dédiées ou au travers d'un fond photographique, les photos et les livres de Hans Silvester sont disponibles à la galerie Retour De Voyage. Nous tenons à votre disposition les photographies originales des peuples de l'Omo et des Benchs, ainsi que des photos moins connues sur le Japon ou encore celles de la pétanque, de la Provence en argentique et bien sûr les photos des chats en Grèce.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph -->
            <p>Nous organisons régulièrement des rencontres avec Hans Silvester, qui malgré son emploi du temps chargé se rend toujours disponible.</p>
            <!-- /wp:paragraph -->

            <!-- wp:paragraph -->
            <p>En 2022, Hans Silvester se voit récompenser pour son travail en obtenant le prix Albert Kahn. L'année 2024 est une année importante où ses images de la pétanque sont présentées sous le label "olympiades culturelles" aux rencontres d'Arles au sein du musée Arlaten et où le château de Gordes présentent dans une scénographie magnifiques les photos du "baladin du monde" tel que l'on qualifie Hans Silvester.</p>
